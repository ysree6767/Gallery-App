class Gallery extends Component {
  state = {
    selectedImage: 0,
  }

  onClickThumbnail = id => {
    this.setState({selectedImage: id})
  }

  render() {
    const {selectedImage} = this.state
    const filteredImageList = imagesList.filter(
      eachImage => eachImage.id === selectedImage,
    )
    const imageUrlLink = filteredImageList[0].imageUrl
    const altImageText = filteredImageList[0].imageAltText

    return (
      <div className="gallery-bg-container">
        <div className="gallery-container">
          <img src={imageUrlLink} alt={altImageText} className="main-image" />
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul id="thumbnails-container">
            {imagesList.map(eachImageItem => (
              <ThumbnailItem
                key={eachImageItem.id}
                thumbnailDetails={eachImageItem}
                isActive={eachImageItem.id === selectedImage}
                onClickThumbnail={this.onClickThumbnail}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery

